# 4. Вводитcя день, месяц и год, необходимо вывести полную дату (обязательно в выводе добавить ".").
# Пример:
# входные данные:
# Введите день: 14
# Введите месяц: 07
# Введите год: 2001
# выходные данные: 14.07.2001
day = int(input("Введите день: "))
month = int(input("Введите месяц: "))
year = int(input("Введите год: "))
print("Дата: ",day,'.',month,".",year)